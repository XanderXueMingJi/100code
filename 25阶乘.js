// 求1+2!+3!+...+20!的和。

// 程序分析：此程序只是把累加变成了累乘。

// !是阶乘的符号.
// 1!=1
// 2!=2*1
// 3!=3*2*1
// ……
// ……
// 19!=19*18*17……3*2*1
// 20!=20*19*18*17……3*2*1
// 然后把它们累加就可以了.
// 1! + 2! + 3! + ... + 20! = 2561327494111820313

var res = 0;
var oldRes = 1;
for(var i = 1; i<=20; i++) {
    for(var j = 1; j <=i; j++) {
        oldRes *=  j;
    }
    res += oldRes;
    oldRes = 1;
}

